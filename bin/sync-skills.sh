#!/usr/bin/env bash

set -euo pipefail

ROOT_DIR="$(cd "$(dirname "$0")/.." && pwd)"
# Windows の Git Bash では pwd (/d/...) と git の --show-toplevel (D:/...) で表記が異なる。
# 非サブモジュール判定で --show-toplevel と比較する際は、同じく git 由来の表記に揃える。
ROOT_TOP="$(git -C "$ROOT_DIR" rev-parse --show-toplevel 2>/dev/null || echo "$ROOT_DIR")"
ROOT_SKILLS_DIR="$ROOT_DIR/.agents/skills"
ROOT_SKILLS_STATE="$ROOT_SKILLS_DIR/.sync-state"
CLAUDE_DIR="$ROOT_DIR/.claude"
CLAUDE_SKILLS_DIR="$CLAUDE_DIR/skills"
IS_WINDOWS=0

if [ "${OS:-}" = "Windows_NT" ]; then
    IS_WINDOWS=1
fi

mkdir -p "$ROOT_SKILLS_DIR"
mkdir -p "$CLAUDE_DIR"

remove_generated_root_skills() {
    if [ ! -f "$ROOT_SKILLS_STATE" ]; then
        return
    fi

    while IFS= read -r line || [ -n "$line" ]; do
        case "$line" in
            "" | \#*)
                continue
                ;;
            *)
                rm -rf "$ROOT_SKILLS_DIR/$line"
                ;;
        esac
    done < "$ROOT_SKILLS_STATE"
}

collect_submodule_skill_dirs() {
    git config --file "$ROOT_DIR/.gitmodules" --get-regexp '^submodule\..*\.path$' 2>/dev/null \
        | awk '{print $2}' \
        | while IFS= read -r submodule_path; do
            [ -n "$submodule_path" ] || continue

            submodule_dir="$ROOT_DIR/$submodule_path"
            if ! git -C "$submodule_dir" rev-parse --show-toplevel >/dev/null 2>&1; then
                continue
            fi

            skill_root="$submodule_dir/.agents/skills"
            if [ ! -d "$skill_root" ]; then
                continue
            fi

            find "$skill_root" -mindepth 1 -maxdepth 1 -type d | sort
        done
}

collect_non_submodule_app_skill_dirs() {
    find "$ROOT_DIR/app" -mindepth 1 -maxdepth 1 -type d | sort \
        | while IFS= read -r app_dir; do
            [ -n "$app_dir" ] || continue

            if [ "$(git -C "$app_dir" rev-parse --show-toplevel 2>/dev/null || true)" != "$ROOT_TOP" ]; then
                continue
            fi

            skill_root="$app_dir/.agents/skills"
            if [ ! -d "$skill_root" ]; then
                continue
            fi

            find "$skill_root" -mindepth 1 -maxdepth 1 -type d | sort
        done
}

collect_skill_dirs() {
    collect_submodule_skill_dirs
    collect_non_submodule_app_skill_dirs
}

# 先頭フロントマターへ `key: "value"` を挿入する。
# 既存の同名キーは置換し、フロントマターが無ければ新規生成する。
inject_frontmatter_key() {
    md_file="$1"
    fm_key="$2"
    fm_value="$3"
    tmp_file="$(mktemp)"
    awk -v key="$fm_key" -v value="$fm_value" '
        BEGIN { newline = key ": \"" value "\""; done = 0; has_fm = 0 }
        NR == 1 {
            if ($0 ~ /^---[[:space:]]*$/) { has_fm = 1; print; next }
            print "---"; print newline; print "---"; print ""; print; done = 1; next
        }
        has_fm && !done && $0 ~ /^---[[:space:]]*$/ { print newline; print; done = 1; next }
        has_fm && !done && index($0, key ":") == 1 { next }
        { print }
    ' "$md_file" > "$tmp_file" && mv "$tmp_file" "$md_file"
}

create_root_skill_entry() {
    src_dir="$1"
    skill_name="$2"
    dst_dir="$ROOT_SKILLS_DIR/$skill_name"

    rm -rf "$dst_dir"

    if [ "$IS_WINDOWS" -eq 1 ]; then
        cp -R "$src_dir" "$dst_dir"
    else
        rel_src="../../${src_dir#$ROOT_DIR/}"
        (
            cd "$ROOT_SKILLS_DIR"
            ln -s "$rel_src" "$skill_name"
        )
    fi
}

recreate_claude_skills() {
    rm -rf "$CLAUDE_SKILLS_DIR"

    if [ "$IS_WINDOWS" -eq 1 ]; then
        cp -R "$ROOT_SKILLS_DIR" "$CLAUDE_SKILLS_DIR"
    else
        (
            cd "$CLAUDE_DIR"
            ln -s ../.agents/skills skills
        )
    fi
}

remove_generated_root_skills

declare -A skill_sources=()

while IFS= read -r skill_dir; do
    [ -n "$skill_dir" ] || continue

    skill_name="$(basename "$skill_dir")"
    if [ -n "${skill_sources[$skill_name]:-}" ]; then
        printf 'ERROR: duplicate collected skill name: %s\n' "$skill_name" >&2
        printf '  %s\n' "${skill_sources[$skill_name]}" >&2
        printf '  %s\n' "$skill_dir" >&2
        exit 1
    fi

    skill_sources["$skill_name"]="$skill_dir"
done < <(collect_skill_dirs)

for skill_name in "${!skill_sources[@]}"; do
    create_root_skill_entry "${skill_sources[$skill_name]}" "$skill_name"
done

{
    printf '# Generated by bin/sync-skills.sh\n'
    for skill_name in "${!skill_sources[@]}"; do
        printf '%s\n' "$skill_name"
    done | sort
} > "$ROOT_SKILLS_STATE"

recreate_claude_skills

# Windows ではコピー (symlink ではない) のため、コピー先からはオリジナルをたどれず、
# .agents/skills 配下は gitignore 対象となる。docsfw の Git リンクを Linux と同じ最終結果に
# するため、コピー先の各 md へ収集元 (オリジナル) の workspace 相対パスを git-origin として埋め込む。
# 注入は recreate_claude_skills の後に行い、.claude/skills (Claude ハーネスが読む) には混入させない。
if [ "$IS_WINDOWS" -eq 1 ]; then
    for skill_name in "${!skill_sources[@]}"; do
        dst_dir="$ROOT_SKILLS_DIR/$skill_name"
        src_rel="${skill_sources[$skill_name]#$ROOT_DIR/}"
        find "$dst_dir" -type f -name '*.md' | while IFS= read -r md_file; do
            md_rel="${md_file#$dst_dir/}"
            inject_frontmatter_key "$md_file" "git-origin" "$src_rel/$md_rel"
        done
    done
fi
