#!/usr/bin/env bash

set -euo pipefail

ROOT_DIR="$(cd "$(dirname "$0")/.." && pwd)"
ROOT_SKILLS_DIR="$ROOT_DIR/.agents/skills"
ROOT_SKILLS_GITIGNORE="$ROOT_SKILLS_DIR/.gitignore"
CLAUDE_DIR="$ROOT_DIR/.claude"
CLAUDE_SKILLS_DIR="$CLAUDE_DIR/skills"
IS_WINDOWS=0

if [ "${OS:-}" = "Windows_NT" ]; then
    IS_WINDOWS=1
fi

mkdir -p "$ROOT_SKILLS_DIR"
mkdir -p "$CLAUDE_DIR"

remove_generated_root_skills() {
    if [ ! -f "$ROOT_SKILLS_GITIGNORE" ]; then
        return
    fi

    while IFS= read -r line || [ -n "$line" ]; do
        case "$line" in
            "" | \#*)
                continue
                ;;
            /*/)
                skill_name="${line#/}"
                skill_name="${skill_name%/}"
                rm -rf "$ROOT_SKILLS_DIR/$skill_name"
                ;;
        esac
    done < "$ROOT_SKILLS_GITIGNORE"
}

collect_submodule_skill_dirs() {
    git config --file "$ROOT_DIR/.gitmodules" --get-regexp '^submodule\..*\.path$' 2>/dev/null \
        | awk '{print $2}' \
        | while IFS= read -r submodule_path; do
            [ -n "$submodule_path" ] || continue

            submodule_dir="$ROOT_DIR/$submodule_path"
            if ! git -C "$submodule_dir" rev-parse --show-toplevel >/dev/null 2>&1; then
                continue
            fi

            skill_root="$submodule_dir/.agents/skills"
            if [ ! -d "$skill_root" ]; then
                continue
            fi

            find "$skill_root" -mindepth 1 -maxdepth 1 -type d | sort
        done
}

create_root_skill_entry() {
    src_dir="$1"
    skill_name="$2"
    dst_dir="$ROOT_SKILLS_DIR/$skill_name"

    rm -rf "$dst_dir"

    cp -R "$src_dir" "$dst_dir"
}

recreate_claude_skills() {
    rm -rf "$CLAUDE_SKILLS_DIR"

    if [ "$IS_WINDOWS" -eq 1 ]; then
        cp -R "$ROOT_SKILLS_DIR" "$CLAUDE_SKILLS_DIR"
    else
        (
            cd "$CLAUDE_DIR"
            ln -s ../.agents/skills skills
        )
    fi
}

remove_generated_root_skills

declare -A skill_sources=()

while IFS= read -r skill_dir; do
    [ -n "$skill_dir" ] || continue

    skill_name="$(basename "$skill_dir")"
    if [ -n "${skill_sources[$skill_name]:-}" ]; then
        printf 'ERROR: duplicate submodule skill name: %s\n' "$skill_name" >&2
        printf '  %s\n' "${skill_sources[$skill_name]}" >&2
        printf '  %s\n' "$skill_dir" >&2
        exit 1
    fi

    skill_sources["$skill_name"]="$skill_dir"
done < <(collect_submodule_skill_dirs)

for skill_name in "${!skill_sources[@]}"; do
    create_root_skill_entry "${skill_sources[$skill_name]}" "$skill_name"
done

{
    printf '# Generated by bin/sync-skills.sh\n'
    for skill_name in "${!skill_sources[@]}"; do
        printf '/%s/\n' "$skill_name"
    done | sort
} > "$ROOT_SKILLS_GITIGNORE"

recreate_claude_skills
